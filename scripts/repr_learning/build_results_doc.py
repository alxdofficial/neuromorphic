#!/usr/bin/env python3
"""Build the centralized head-to-head results doc from saved per-sample files.

Three metrics (the ones we report):
  EM          — exact match (strict, normalized).
  Containment — gold answer appears as a whole-token span in the output
                (verbosity-robust, factually strict; whole-word fix applied).
  Judge       — LLM judge (Gemini 3 Flash, 7-category rubric → score; paraphrase- AND
                verbosity-robust). Categories saved so scores are re-weightable.

EM + Containment are RECOMPUTED here from rescore predictions with the current (fixed)
metric functions, so the doc never carries the old substring-containment false positives.
"""
import sys, json
from pathlib import Path
from collections import defaultdict, Counter
ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT))
from scripts.repr_learning.eval_per_family import em_score, containment_score, max_over_refs

EVAL = ROOT / "outputs/repr_learning/eval_per_family"
RESCORE = EVAL / "rescore_newmetric_per_sample.jsonl"
JUDGE = EVAL / "judge_full_per_sample.jsonl"
DOC = ROOT / "docs/repr_learning_head_to_head.md"

DISP = {"graph_v5_baseline": "graph_v5", "recurrent_baseline": "Mamba", "flat_baseline": "flat",
        "continuous_baseline": "continuous", "memorizing_baseline": "MT"}
ORDER = ["graph_v5_baseline", "recurrent_baseline", "flat_baseline", "continuous_baseline", "memorizing_baseline"]
FAMS = ["biographical", "hotpot_qa", "musique", "narrative_qa", "babilong"]
CATS = ["correct", "correct_verbose", "incomplete", "partially_wrong", "wrong_value", "irrelevant", "no_answer"]

rescore = [json.loads(l) for l in open(RESCORE)]
judge = [json.loads(l) for l in open(JUDGE)]

em = defaultdict(list); con = defaultdict(list)
con_fam = defaultdict(lambda: defaultdict(list))
for r in rescore:
    e = max_over_refs(r["pred"], r["refs"], em_score)
    c = max_over_refs(r["pred"], r["refs"], containment_score)
    em[r["variant"]].append(e); con[r["variant"]].append(c)
    con_fam[r["variant"]][r["family"]].append(c)

jsc = defaultdict(list); jcat = defaultdict(Counter)
for j in judge:
    if j.get("score") is not None and not str(j.get("category")).startswith("__FAIL__"):
        jsc[j["variant"]].append(j["score"]); jcat[j["variant"]][j["category"]] += 1


def pct(xs):
    return 100 * sum(xs) / len(xs) if xs else 0.0


rows = sorted(ORDER, key=lambda v: -pct(jsc[v]))
L = []
L.append("# Memory architectures — head-to-head results\n")
L.append("_Generated by `scripts/repr_learning/build_results_doc.py` from the saved per-sample files._\n")
L.append("**Setup:** frozen Llama-3.2-1B decoder + a side-car memory (128 tokens × d_llama, ~49M params each, "
         "matched). 5 families × 48 questions = **240 samples per model**. babiLong is held-out OOD.\n")

L.append("\n## Metrics we report\n")
L.append("| metric | what it measures | punishes verbosity? | punishes paraphrase? |")
L.append("|---|---|---|---|")
L.append("| **EM** | exact match after normalization | yes (strict) | yes |")
L.append("| **Containment** | gold answer appears as a whole-token span in the output | **no** | yes |")
L.append("| **Judge** | LLM judge (Gemini 3 Flash), 7-category rubric → score | **no** | **no** |")
L.append("\nJudge is the headline (matches the goal: *did the model give the right answer, regardless of phrasing?*). "
         "EM/Containment are the strict, free, deterministic lexical floor. (EM ≤ Containment ≤ Judge by construction.)\n")

L.append("\n## Headline (macro over 240 samples, %)\n")
L.append("| model | EM | Containment | **Judge** |")
L.append("|---|---|---|---|")
for v in rows:
    L.append(f"| {DISP[v]} | {pct(em[v]):.1f} | {pct(con[v]):.1f} | **{pct(jsc[v]):.1f}** |")
L.append("\n**Verdict:** all five cluster at ~12% (Judge); **graph_v5 is mid-pack, not winning.** "
         "Even with paraphrase credited and verbosity forgiven, the structured graph memory does not beat the "
         "flat/recurrent baselines.\n")

L.append("\n## Containment by family (%)\n")
L.append("| model | " + " | ".join(f.replace('_qa', '').replace('_', ' ') for f in FAMS) + " |")
L.append("|" + "---|" * (len(FAMS) + 1))
for v in rows:
    cells = " | ".join(f"{pct(con_fam[v][f]):.1f}" for f in FAMS)
    L.append(f"| {DISP[v]} | {cells} |")
L.append("\nBiographical is ~0 for everyone — it's the only *uncontaminated* family (synthetic, no Llama priors) "
         "and it over-subscribes the 128-slot budget, so it exposes the true memory floor.\n")

L.append("\n## LLM-judge category breakdown (counts /240) — the per-case detail\n")
L.append("Score mapping: correct=1.0, correct_verbose=1.0, incomplete=0.5, partially_wrong=0.25, "
         "wrong_value=0.0, irrelevant=0.0, no_answer=0.0 (re-weightable — categories are saved per sample).\n")
L.append("| model | correct | verbose | incomplete | partial_wrong | **wrong_value** | irrelevant | no_answer |")
L.append("|---|---|---|---|---|---|---|---|")
for v in rows:
    c = jcat[v]
    L.append(f"| {DISP[v]} | {c['correct']} | {c['correct_verbose']} | {c['incomplete']} | "
             f"{c['partially_wrong']} | **{c['wrong_value']}** | {c['irrelevant']} | {c['no_answer']} |")
L.append("\n**The real story:** across every model **~83% of answers are `wrong_value`** — the right *type* of "
         "answer (a year/name/place) with the wrong *value* = confident confabulation. Only ~10% are genuinely "
         "`correct`. These memories mostly *confabulate plausibly* rather than retrieve.\n")

L.append("\n## Provenance\n")
L.append("- **Models / best-val step:** graph_v5 @7000, Mamba @6500, flat @5000, continuous @7000, MT @5500 "
         "(`outputs/repr_learning/{v56_graph_v5_*, tranche6_*}`).\n")
L.append("- **Predictions:** `rescore_newmetric_per_sample.jsonl` (greedy AR-decode, 40 max-new-tokens).\n")
L.append("- **Judge:** `google/gemini-3-flash-preview` via OpenRouter, structured output, temp=0/seed=0, "
         "1200 judgments, **$0.23**, 96s, 0 failures → `judge_full_per_sample.jsonl`.\n")
L.append("- **Metric code:** `scripts/repr_learning/eval_per_family.py` (em/containment/recall/f1, "
         "regression-tested in `tests/test_eval_metrics.py`); judge in `scripts/repr_learning/llm_judge.py`.\n")

DOC.write_text("\n".join(L) + "\n")
print(f"wrote {DOC}")
print("\n--- headline ---")
for v in rows:
    print(f"  {DISP[v]:11s}  EM={pct(em[v]):4.1f}  Contain={pct(con[v]):4.1f}  Judge={pct(jsc[v]):4.1f}   "
          f"wrong_value={jcat[v]['wrong_value']}/240")
